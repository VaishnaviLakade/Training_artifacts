// 3. Variables
let name = "Vaishnavi";
const age = 21;
var city = "Pune";

console.log("Name:", name);
console.log("Age:", age);
console.log("City:", city);

// 4. Operators
let a = 10;
let b = 5;

console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);

// 5. Loops

// for loop
for (let i = 1; i <= 3; i++) {
  console.log("For Loop:", i);
}

// while loop
let i = 1;
while (i <= 3) {
  console.log("While Loop:", i);
  i++;
}

